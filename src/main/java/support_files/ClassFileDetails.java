package support_files;

public class ClassFileDetails {
    public String NAME;
    public int ID;
    public String IP;
    public String OFFICE;


    public void setId(Integer ID) {
        this.ID = ID;
    }

    public void setName(String NAME) {
        this.NAME = NAME;
    }

    public void setIp(String IP) {
        this.IP = IP;
    }

    public void setOffice(String OFFICE) {
        this.OFFICE = OFFICE;
    }
}
